	public static boolean search(int[] nums, int target) {
		if (nums == null || nums.length == 0)
			return false;
		int low, high, mid;
		low = 0;
		high = nums.length - 1;
		while (low <= high) {
			mid = low + (high - low) / 2;
			if (nums[mid] == target) {
				return true;
			} else {
				// 第一种情况，前面的部分是局部有序
				if (nums[mid] > nums[low]) {
					if (nums[low] <= target && target <= nums[mid]) {
						high = mid - 1;
					} else {
						low = mid + 1;
					}
				} else {// 第二种情况，后面的部分是局部有序
					if (nums[mid] < nums[low]) {
						if (nums[mid] <= target && target <= nums[high]) {
							low = mid + 1;
						} else {
							high = mid - 1;
						}
					}else{//这个是相等的情况
						//nums[mid] = nums[low]
						low++;
					}
				}
			}
		}
		return false;
	}
